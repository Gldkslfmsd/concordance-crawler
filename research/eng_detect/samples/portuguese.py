"""A sociologia é a parte das ciências humanas que estuda o comportamento
humano em função do meio e os processos que interligam os indivíduos em
associações, grupos e instituições. Enquanto o indivíduo na sua
singularidade é estudado pela psicologia, a sociologia tem uma base
teórico-metodológica voltada para o estudo dos fenômenos sociais, tentando
explicá-los e analisando os seres humanos em suas relações de
interdependência. Compreender as diferentes sociedades e culturas é um dos
objetivos da sociologia.

Os resultados da pesquisa sociológica não são de interesse apenas de
sociólogos(as). Cobrindo todas as áreas do convívio humano — desde as
relações na família até a organização das grandes empresas, o papel da
política na sociedade ou o comportamento religioso —, a sociologia pode vir
a interessar, em diferentes graus de intensidade, a diversas outras áreas do
saber. Entretanto, os maiores interessados na produção e sistematização do
conhecimento sociológico atualmente são o Estado, normalmente o principal
financiador da pesquisa desta disciplina científica, e a sociedade civil
organizada (movimentos sociais por exemplo).

Assim como toda ciência, a sociologia pretende explicar a totalidade do seu
universo de pesquisa. Ainda que esta tarefa não seja objetivamente
alcançável, é tarefa da sociologia transformar as malhas da rede com a qual
ela capta a realidade social cada vez mais estreitas. Por essa razão,
o conhecimento sociológico, através dos seus conceitos, teorias e métodos,
pode constituir para as pessoas um excelente instrumento de compreensão das
situações com que se defrontam na vida cotidiana, das suas múltiplas
relações sociais e, consequentemente, de si mesmas como seres
inevitavelmente sociais.

A sociologia ocupa-se, ao mesmo tempo, das observações do que é repetitivo
nas relações sociais para daí formular generalizações teóricas; e também se
interessa por eventos únicos sujeitos à inferência sociológica (como, por
exemplo, o surgimento do capitalismo ou a gênese do Estado Moderno),
procurando explicá-los no seu significado e importância singulares).

A sociologia surgiu como uma disciplina a partir de fins do século XVIII, na
forma de resposta acadêmica para um desafio de modernidade: se o mundo está
ficando mais integrado, a experiência de pessoas do mundo é crescentemente
atomizada e dispersada. Sociólogos não só esperavam entender o que unia os
grupos sociais, mas também desenvolver um "antídoto" para a desintegração
social.

Hoje os sociólogos pesquisam macroestruturas inerentes à organização da
sociedade, como raça ou etnicidade, classe e gênero, além de instituições
como a família; processos sociais que representam divergência, ou
desarranjos, nestas estruturas, inclusive crime e divórcio; e microprocessos
como relações interpessoais.

A sociologia pesquisa também as estruturas de força e de poder do Estado
e de seus membros, e a forma como o poder se estrutura através de
microrrelações de forças. Um dos aspectos que tem sido alvo dos estudos da
sociologia, e também da antropologia, é a forma como os indivíduos
constituintes da sociedade podem ser manipulados para a manutenção da ordem
social e do monopólio da força física legitimada.[1]

Sociólogos fazem uso frequente de técnicas quantitativas de pesquisa social
(como a estatística) para descrever padrões generalizados nas relações
sociais. Isto ajuda a desenvolver modelos que possam entender mudanças
sociais e como os indivíduos responderão a essas mudanças. Em alguns campos
de estudo da sociologia, as técnicas qualitativas — como entrevistas
dirigidas, discussões em grupo e métodos etnográficos — permitem um melhor
entendimento dos processos sociais de acordo com o objetivo explicativo.""",

